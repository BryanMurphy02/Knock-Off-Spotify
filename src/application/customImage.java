package application;

import javafx.scene.image.ImageView;

public class customImage {
	
	ImageView image;
	
	public customImage(ImageView imageView) {
		this.image = image;
	}

	
	public void setImage(ImageView value) {
		image = value;
	}
	
	public ImageView getImage() {
		return image;
	}

}
